#include "libft.h"

void	*ft_memchr(const void *ptr, int c, size_t n)
{
	while (n--)
	{
		if (*(unsigned char *)ptr++ == (unsigned char)c)
			return ((void *)(unsigned char *)ptr - 1);
	}
	return (NULL);
}
