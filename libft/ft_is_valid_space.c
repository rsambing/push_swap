int	ft_is_valid_space(const char *str, int i)
{
	if ((str[i] == 32 || (str[i] >= 9 && str[i] <= 13)) && (str[i + 1] >= 33
			&& str[i + 1] <= 126) && (str[i - 1] >= 33 && str[i - 1] <= 126))
		return (1);
	return (0);
}
